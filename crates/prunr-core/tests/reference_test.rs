// LOAD-04: Large image handling
// ============================================================

#[test]
fn test_large_image_warning() {
    // check_large_image returns LargeImage error for > 8000px images
    let large = DynamicImage::ImageRgb8(image::RgbImage::new(9000, 100));
    let err = check_large_image(&large);
    assert!(err.is_some(), "Expected LargeImage warning for 9000px image");
    match err.unwrap() {
        CoreError::LargeImage { width, height: _, limit } => {
            assert_eq!(width, 9000);
            assert_eq!(limit, LARGE_IMAGE_LIMIT);
        }
        other => panic!("Expected LargeImage, got {:?}", other),
    }
}

#[test]
fn test_downscale_image_preserves_aspect_ratio() {
    // 8000x4000 → max_dim 4096 → 4096x2048 (aspect preserved)
    let img = DynamicImage::ImageRgb8(image::RgbImage::new(8000, 4000));
    let scaled = downscale_image(img, DOWNSCALE_TARGET);

    assert!(
        scaled.width().max(scaled.height()) <= DOWNSCALE_TARGET,
        "Downscaled max dimension {} exceeds {}",
        scaled.width().max(scaled.height()), DOWNSCALE_TARGET
    );

    let aspect_original = 8000.0_f64 / 4000.0;
    let aspect_scaled = scaled.width() as f64 / scaled.height() as f64;
    assert!(
        (aspect_original - aspect_scaled).abs() < 0.02,
        "Aspect ratio changed too much: {aspect_original:.3} → {aspect_scaled:.3}"
    );
}

